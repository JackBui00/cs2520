import turtle as t

def print_start_stop():
    t.speed(1)
    t.forward(20)
    t.backward(20)
    t.right(90)

def print_zero():
    t.speed(1)
    t.forward(10)
    t.left(90)
    t.forward(10)
    t.backward(10)
    t.right(90)


def print_one():
    t.speed(1)
    t.forward(10)
    t.left(90)
    t.forward(20)
    t.backward(20)
    t.right(90)


